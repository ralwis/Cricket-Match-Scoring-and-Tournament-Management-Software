    private void playernameaddBTNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_playernameaddBTNMouseClicked
        if (playernumber.getText().isEmpty() || playername.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Add Player Name and Number"); 
        }else{
            try {
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/atmdb","root","");
                PreparedStatement Add = Con.prepareStatement("Insert into playername values(?,?,?,?,?,?)");
                Add.setString(1, emailaddress);
                Add.setString(2, tourname);
                Add.setString(3, teamname.getText());
                Add.setInt(4, Integer.valueOf(numofplayers.getText()));
                Add.setInt(5, Integer.valueOf(playernumber.getText()));
                Add.setString(6, playername.getText());
                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Player Name and Number Saved");
                Con.close();
                //Clear();
                tablerowCount();
                playercount.setText(""+rightrowcount);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_playernameaddBTNMouseClicked

