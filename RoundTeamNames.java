    private void teamnameaddBTNMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_teamnameaddBTNMouseClicked
        if (teamname.getText().isEmpty() || numofplayers.getText().isEmpty() || numofovers.getText().isEmpty() || ballsperover.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this, "Please Add Empty Values"); 
        }else{
            try {
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/atmdb","root","");
                PreparedStatement Add = Con.prepareStatement("Insert into teamnames values(?,?,?,?,?,?)");
                Add.setString(1, emailaddress);
                Add.setString(2, tourname);
                Add.setString(3, teamname.getText());
                Add.setInt(4, Integer.valueOf(numofplayers.getText()));
                Add.setInt(5, Integer.valueOf(numofovers.getText()));
                Add.setInt(6, Integer.valueOf(ballsperover.getText()));
                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Saved");
                Con.close();
                //Clear();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_teamnameaddBTNMouseClicked

